package net.gudenau.cavegame.gui.input;

/// An enumeration of mouse buttons that are supported by the engine.
public enum MouseButton {
    /// The left mouse button, normally used for left-clicking.
    LEFT,
    /// The right mouse button, normally used for right-clicking.
    RIGHT,
    /// The middle mouse button, normally part of the scroll wheel.
    MIDDLE,
    /// The fourth mouse button, normally on the side and used to navigate back.
    FOUR,
    /// The fifth mouse button, normally on the side and used to navigate forward.
    FIVE,
}
